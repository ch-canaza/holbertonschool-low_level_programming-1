/**
 * print_timetable - times table
 * @n: multiplication times.
 *
 * Return: nothing
 *
 */

void print_times_table(int n)
{
	int i;
	int k;
	int j;

	for (i = 0; i <= n; i++)
	{
		for (k = 0; k <= n; k++)
		{
			j = i * k;

			if (j == 0)
			{
				_putchar(j + '0');
			}
			else if (j <= 9)
			{
				_putchar(',');
				_putchar(' ');
				_putchar(' ');
				_putchar(j + '0');
			}
			else if (j >= 10)
			{
				_putchar(',');
				_putchar(' ');
				_putchar(j / 10 + '0');
				_putchar(j % 10 + '0');
			}

		}
		_putchar('\n');
	}

}
